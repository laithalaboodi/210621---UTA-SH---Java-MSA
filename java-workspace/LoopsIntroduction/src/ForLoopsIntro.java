
public class ForLoopsIntro {
	public static void main(String[] args) {
		/*
		 * for (initialization statement; condition; update statement){
		 *    instructions
		 * }
		 */
		int i; 
		for(i=0;i<=10;i++) {
			System.out.println(i);
		}
		//System.out.println(i);
		/*
		 * Enhanced for loop
		 */
		
		
	}
}
