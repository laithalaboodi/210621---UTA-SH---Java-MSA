
public class WhileLoopIntro {
	public static void main(String[] args) {
		/*
		 * while(conditional statement){
		 * 	 instructions that we want repeat as long
		 *   as our statement is true 
		 * }
		 */
		
		int x = 1; 
		while(x<9) {
			System.out.println(x);
			x+=1; //same as x = x+1; 
		}
		//x
	
	}
}
